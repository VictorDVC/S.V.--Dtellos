    public void calcularPrecio(){
        
        //el precio va a ser igual al mismo indice del arreglo productos con el arreglo precios
        precio = precios[cbxProductos.getSelectedIndex()];
        
        cantidad = Integer.parseInt(spnCantidad.getValue().toString());
        
        lblPrecio.setText("S/"+precio);
        lblImporte.setText("S/"+precio*cantidad);
    }
    public void actualizarTabledeventa(){
        //Para evitar que se añada una compra ya ingresada
        while (modelo.getRowCount()>0) {            
            modelo.removeRow(0);
        }
        double subTotal=0;
        for (Venta v : listaventa) {
            //Creando un array de objetos
            Object oa[]= new Object[4];
            oa[0]=v.getNombreP();
            oa[1]=v.getPrecio();
            oa[2]=v.getCantidad();
            oa[3]=v.getImporte();
            //Al atributo subtotal se le va ir sumando el importe que se vaya añadiendo
            subTotal+=v.getImporte();
            
            //al modelo para el table le vamos a añadir las filas del array
            modelo.addRow(oa);
        }
        double iva = subTotal*0.19;
        double total=subTotal+iva;
        tfdSubTotal.setText(String.valueOf(String.format("%.2f", subTotal)));
        tfdIGV.setText(String.valueOf(String.format("%.2f", iva)));
        tfdTotalPagar.setText(String.valueOf(String.format("%.2f", total)));
        tbeCompra.setModel(modelo);
    }
    public boolean comprobarVenta(Venta venta){
        for (Venta v : listaventa) {
            if (v.getCodigo()==venta.getCodigo()) {
                int nuevaCant = v.getCantidad()+venta.getCantidad();
                v.setCantidad(nuevaCant);
                v.setImporte(v.getPrecio()*nuevaCant);
                return true;
            }
        }
        return false;
    }
    public void eliminarfilaventa(){
        int fila = tbeCompra.getSelectedRow();
        if (tbeCompra.getSelectedRow()>=0) {
            modelo.removeRow(fila);
    }else{
            JOptionPane.showMessageDialog(null, "Seleccionar fila");
        }
    }
