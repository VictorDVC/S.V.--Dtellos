    private void btnFinalizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarCompraActionPerformed

        if (tfdNombreC.getText().equalsIgnoreCase("")||tfdDni.getText().equalsIgnoreCase("")||tfdNumeroC.getText().equalsIgnoreCase("")||tfdTotalPagar.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Falta de datos");
        }else{
            if (tfdDni.getText().length()!=8 || tfdNumeroC.getText().length()!=9) {
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
            }else{
        Datos d = new Datos();
        d.InsertarRegistro(tfdNombreC, tfdDni, tfdNumeroC, tfdTotalPagar);
        d.Mostrar(tbeRegistro);
        tfdNombreC.setText("");
        tfdDni.setText("");
        tfdNumeroC.setText("");
        JOptionPane.showMessageDialog(null, "Compra Realizada");
        int filas = tbeCompra.getRowCount();
        for (int i = filas-1; i>=0; i--) {
            modelo.removeRow(i);
        }
    }
    }
    }//GEN-LAST:event_btnFinalizarCompraActionPerformed

